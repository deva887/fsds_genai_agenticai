#type conversions from any to int
print(int(2.9))#float to int
print(int(2))#int to int
print(int('2'))#string to int only applicable for numercical strings
print(int(True))#bool to int
print(int(False))#bool to int
print(int('100'))#string to int
# print(int(3+2j)) not possible to convert complex to int-------------------------------------------------
#type conversion from any to float
print(float(2))
print(float(2.2))
print(float(True))
print(float('2'))
print(float())
# print(float(3+3j))#not possible to convert complex to float------------------------------------------
